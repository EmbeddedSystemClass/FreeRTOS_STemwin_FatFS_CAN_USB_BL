/*
*********************************************************************************************************
*	函 数 名: _cbGetData16
*	功能说明: XBF字体的回调函数, 16点阵
*	形    参: Off      - 地址偏移 
*             NumBytes - 需要读出的字节数
*             pVoid    - 指针变量，一般用于带文件系统时的FIL类型变量
*             pBuffer  - 获取字体的点阵数据
*	返 回 值: 0 表示成功 1 表示失败
*********************************************************************************************************
*/
static int _cbGetData20(U32 Off, U16 NumBytes, void * pVoid, void * pBuffer)
{	
	/* 读取点阵数据 */
	sf_ReadBuffer(pBuffer, XBF_Font20BaseAdd + Off, NumBytes);
	return 0;
}

